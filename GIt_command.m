%% =========================================================================
%% 1. 分支管理（创建 / 切换 / 重命名 / 查看）
%% =========================================================================

!git branch
% 查看本地所有分支（当前分支带 *）

!git switch main
% 切换到 main 分支

!git switch branch_3
% 切换到指定分支

!git switch -c branch_4
% 基于当前分支创建并切换到新分支

!git branch -m old_name new_name
% 重命名分支

%% =========================================================================
%% 2. 分支删除（本地 / 远程）
%% =========================================================================

!git branch -d branch_4
% 删除本地分支（仅允许删除已合并分支）

!git branch -D b3
% 强制删除本地分支（无论是否合并）

!git push origin --delete b3
% 删除远程分支


%% =========================================================================
%% 3. 查看仓库状态（建议高频使用）
%% =========================================================================

!git status
% 查看当前修改状态（最重要命令）

!git branch
% 查看当前所在分支

!git log -1
% 查看最近一次提交记录

!git log --oneline --graph --all
% 图形化查看提交历史（推荐）


%% =========================================================================
%% 4. 提交代码（add → commit → push）
%% =========================================================================

!git add .
% 添加所有修改到暂存区

msg = ['update ', datestr(now,'yyyymmdd HH.MM')];
system(['git commit -m "', msg, '"'])% 标准提交（推荐）

!git push
% 推送当前分支到远程

!git push -u origin branch_4
% 首次推送并建立远程跟踪关系


%% =========================================================================
%% 5. 更新代码（pull / fetch）
%% =========================================================================

!git fetch
% 获取远程更新，但不合并

!git pull
% 拉取当前跟踪分支最新代码

%% =========================================================================
%% 6. 撤销与恢复（危险操作）
%% =========================================================================

!git checkout .
% 撤销工作区所有未暂存修改（危险）

!git restore .
% 新版推荐：撤销工作区修改

!git reset --hard
% 回退到最近一次 commit（危险，未提交内容丢失）

!git reset --hard HEAD~1
% 回退到上一个提交（危险）

!git clean -fd
% 删除未跟踪文件（危险）